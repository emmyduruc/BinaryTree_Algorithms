package com.devfootcode;

public class Node {
    private Node parent;
    private Node left;
    private Node right;
    private int key;

    public Node(int key) {
        this.key = key;
    }

    public Node getParent() {
        return parent;
    }

    public void setParent(Node parent) {
        this.parent = parent;
    }

    public Node getLeft() {
        return left;
    }

    public void setLeft(Node left) {
        this.left = left;
    }

    public Node getRight() {
        return right;
    }

    public void setRight(Node right) {
        this.right = right;
    }

    public int getKey() {
        return key;
    }

    public void setKey(int key) {
        this.key = key;
    }

    @Override
    public String toString() {
        return "Node [parent=" + parent + ", left=" + left + ", right=" + right + ", key=" + key + "]";
    }

}
